package javaprograms;

public class switchprogram {

	public static void main(String[] args) {
		int a=1;
		switch(a)
		{
			case 1:
				System.out.println("first class");
				break;
			case 2:
				System.out.println("second class");
				break;
				default:
					System.out.println("wrong value");
			
		}

	}

}
